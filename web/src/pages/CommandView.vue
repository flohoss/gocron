<script setup lang="ts">
import { useEventSource, useMagicKeys } from '@vueuse/core';
import CommandWindow from '../components/utils/CommandWindow.vue';
import { BackendURL } from '../main';
import { ref, watch, watchEffect } from 'vue';
import { FontAwesomeIcon } from '@fortawesome/vue-fontawesome';
import { faArrowDown, faArrowUp, faTerminal } from '@fortawesome/free-solid-svg-icons';
import { postCommand } from '../client/sdk.gen';
import { GetColor, Severity } from '../severity';

type CommandInfo = {
  severity: Severity;
  command: string;
};

const { data, close } = useEventSource(BackendURL + '/api/events?stream=command', [], {
  autoReconnect: { delay: 100 },
});
addEventListener('beforeunload', () => {
  close();
});

watch(data, () => {
  const parsedResponse = JSON.parse(data.value);
  if (!parsedResponse) return;
  responses.value.push(parsedResponse);
});

const responses = ref<CommandInfo[]>([]);
const command = ref('');
const history = ref<string[]>([]);
const historyIndex = ref(-1);

function executeCommand() {
  postCommand({
    body: {
      command: command.value,
    },
  });
  history.value.push(command.value);
  historyIndex.value = -1;
  command.value = '';
}

function navigateHistory(direction: string) {
  if (direction === 'up') {
    if (historyIndex.value < history.value.length - 1) {
      historyIndex.value++;
      command.value = history.value[history.value.length - 1 - historyIndex.value];
    }
  } else if (direction === 'down') {
    if (historyIndex.value > 0) {
      historyIndex.value--;
      command.value = history.value[history.value.length - 1 - historyIndex.value];
    } else {
      historyIndex.value = -1;
      command.value = '';
    }
  }
}

const { ctrl, l } = useMagicKeys();

watchEffect(() => {
  if (ctrl.value && l.value) responses.value = [];
});
</script>

<template>
  <CommandWindow :stickToBottom="true" title="Terminal">
    <pre v-for="(response, index) in responses" :key="index" :class="GetColor(response.severity)" class="flex">
      <code>{{ response.command }}</code>
    </pre>
    <template v-slot:top>
      <div class="hidden md:flex flex-wrap items-center gap-10 text-xs">
        <div class="flex items-center gap-2">
          Press
          <kbd class="kbd kbd-xs">ctrl</kbd>
          +
          <kbd class="kbd kbd-xs">l</kbd>
          to clear terminal
        </div>
        <div class="flex items-center gap-2">
          Press
          <kbd class="kbd kbd-xs"><FontAwesomeIcon :icon="faArrowUp" /></kbd>
          or
          <kbd class="kbd kbd-xs"><FontAwesomeIcon :icon="faArrowDown" /></kbd>
          to navigate history
        </div>
      </div>
    </template>
    <template v-slot:bottom>
      <div class="grid gap-5">
        <label class="input w-full">
          <FontAwesomeIcon :icon="faTerminal" />
          <input
            @keydown.up.prevent="navigateHistory('up')"
            @keydown.down.prevent="navigateHistory('down')"
            @keydown.esc="command = ''"
            @keydown.enter="executeCommand"
            v-model="command"
            autofocus
            type="text"
            placeholder="Command"
          />
        </label>
      </div>
    </template>
  </CommandWindow>
</template>
